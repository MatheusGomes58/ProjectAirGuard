_CASET        = const(0x2A)
_RASET        = const(0x2B)
_RAMWR        = const(0x2C)
_MADCTL       = const(0x36)
_COLMOD       = const(0x3A)
_TEON         = const(0x35)

# Rotation MADCTL values (MY, MX, MV, ML, BGR, MH bits)
# Ajustados para evitar o efeito espelhado (Mirroring)
_ROTATIONS = [0x48, 0x28, 0x88, 0xE8]

# ---------------------------------------------------------------------------
# Colour helpers (RGB565)
# ---------------------------------------------------------------------------
