    def _set_window(self, x0, y0, x1, y1):
        self._cmd(_CASET); self._data(ustruct.pack(">HH", x0, x1))
        self._cmd(_RASET); self._data(ustruct.pack(">HH", y0, y1))
        self._cmd(_RAMWR)

    def fill(self, color):
        self._set_window(0, 0, 239, 239)
        color_bytes = ustruct.pack(">H", color)
        line = color_bytes * 240
        self.dc.value(1)
        self.cs.value(0)
        for _ in range(240):
            self.spi.write(line)
        self.cs.value(1)

    def fill_rect(self, x, y, w, h, color):
        x1 = min(x + w - 1, 239); y1 = min(y + h - 1, 239)
        self._set_window(x, y, x1, y1)
        color_bytes = ustruct.pack(">H", color)
        count = (x1 - x + 1) * (y1 - y + 1)
        self.dc.value(1); self.cs.value(0)
        for _ in range(count): self.spi.write(color_bytes)
        self.cs.value(1)

    def text(self, s, x, y, color, scale=1):
        # Simplest font rendering (subset)
        for char in s:
            self._draw_char(x, y, char, color, scale)
            x += 6 * scale

