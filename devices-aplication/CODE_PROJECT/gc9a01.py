# GC9A01 Round Display Driver for MicroPython (Raspberry Pi Pico W)
# Resolution: 240 x 240 pixels (round)
# Interface: SPI
#
# Pinout used:
#   SCL (SCK)  -> GP18
#   SDA (MOSI) -> GP19
#   CS         -> GP17
#   DC         -> GP20
#   RST        -> GP21

import time
